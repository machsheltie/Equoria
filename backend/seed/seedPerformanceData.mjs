/**
 * Main performance data seeding function
 */
async function seedPerformanceData() {
  try {
    logger.info('🚀 Starting performance data seeding...');
    logger.info('=====================================');

    // Clean existing performance test data
    logger.info('🧹 Cleaning existing performance test data...');
    // CompetitionResult has no direct `user` relation — only `horse` and
    // `show`. Reach the user via horse.user, matching the trainingLog clean
    // immediately below. (Equoria-3iyk Phase 1c — schema drift from a
    // previous refactor that removed CompetitionResult.userId.)
    await prisma.competitionResult.deleteMany({
      where: {
        horse: {
          user: {
            email: {
              startsWith: 'perftest',
            },
          },
        },
      },
    });

    await prisma.trainingLog.deleteMany({
      where: {
        horse: {
          user: {
            email: {
              startsWith: 'perftest',
            },
          },
        },
      },
    });

    await prisma.groom.deleteMany({
      where: {
        user: {
          email: {
            startsWith: 'perftest',
          },
        },
      },
    });

    await prisma.horse.deleteMany({
      where: {
        user: {
          email: {
            startsWith: 'perftest',
          },
        },
      },
    });

    // Show's user relation is `hostUser` (FK: hostUserId), not `host`.
    // Equoria-3iyk Phase 1c (continuation of competitionResult fix).
    await prisma.show.deleteMany({
      where: {
        hostUser: {
          email: {
            startsWith: 'perftest',
          },
        },
      },
    });

    await prisma.user.deleteMany({
      where: {
        email: {
          startsWith: 'perftest',
        },
      },
    });

    // Seed performance test data
    await seedPerformanceUsers();
    await seedPerformanceHorses();
    await seedPerformanceShows();
    await seedPerformanceCompetitionResults();
    await seedPerformanceGrooms();
    await seedPerformanceTrainingSessions();

    logger.info('✅ Performance data seeding completed successfully!');
    logger.info('📊 Performance test database ready for load testing');
  } catch (error) {
    logger.error('❌ Performance data seeding failed:', error);
    throw error;
  } finally {
    await prisma.$disconnect();
  }
}

// Run seeding if this script is executed directly. Use pathToFileURL to
// correctly compare on Windows (where `process.argv[1]` uses backslashes
// but `import.meta.url` uses URL-encoded forward slashes — the simple
// `file://${argv[1]}` template never matches on Windows, silently making
// the script a no-op when invoked via `node seed/seedPerformanceData.mjs`).
if (import.meta.url === pathToFileURL(process.argv[1]).href) {
  seedPerformanceData()
    .then(() => {
      console.log('✅ Performance data seeding completed');
      process.exit(0);
    })
    .catch(error => {
      console.error('❌ Performance data seeding failed:', error);
      process.exit(1);
    });
}

export { seedPerformanceData };
