function geneWeights(spec, gene) {
  let w;
  if (gene === 'E_Extension') w = { 'e/e': spec.E[0], 'E/e': spec.E[1], 'E/E': spec.E[2] };
  else if (gene === 'A_Agouti') w = { 'a/a': spec.A[0], 'A/a': spec.A[1], 'A/A': spec.A[2] };
  else w = spec.w[gene] ?? NEG[gene];
  // drop zero-weight alleles so allowed_alleles stays clean (e.g. Suffolk chestnut-fixed)
  return Object.fromEntries(Object.entries(w).filter(([, v]) => v > 0));
}

