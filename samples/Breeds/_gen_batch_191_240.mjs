// ---- emit ---------------------------------------------------------------
function geneWeights(spec, gene) {
  if (gene === 'E_Extension') return { 'e/e': spec.E[0], 'E/e': spec.E[1], 'E/E': spec.E[2] };
  if (gene === 'A_Agouti') return { 'a/a': spec.A[0], 'A/a': spec.A[1], 'A/A': spec.A[2] };
  if (spec.w[gene]) return spec.w[gene];
  return NEG[gene];
}

let errors = 0;
for (const spec of B) {
  const allowed = {};
  const weights = {};
  for (const gene of GENE_ORDER) {
    const w = geneWeights(spec, gene);
    allowed[gene] = Object.keys(w);
    weights[gene] = w;
    const sum = Object.values(w).reduce((a, b) => a + b, 0);
    if (Math.abs(sum - 1) > 0.0005) {
      console.log(`WEIGHTSUM ${spec.n} ${gene}=${sum.toFixed(4)}`);
      errors++;
    }
  }
  const tSum = Object.values(spec.temp).reduce((a, b) => a + b, 0);
  if (tSum !== 100) {
    console.log(`TEMPSUM ${spec.n}=${tSum}`);
    errors++;
  }

  const hasLP = !!spec.w.LP_LeopardComplex;
  const mk = MARK[spec.mark];
  const shade = {};
  for (const s of spec.shades) {
    if (!SH[s]) {
      console.log(`BADSHADE ${spec.n} "${s}"`);
      errors++;
      continue;
    }
    shade[s] = SH[s];
  }
  const gaited = spec.gaits.gaiting !== null;

  const profile = {
    allowed_alleles: allowed,
    disallowed_combinations: {},
    allele_weights: weights,
    marking_bias: {
      face: mk.face,
      legs_general_probability: mk.legs,
      leg_specific_probabilities: mk.leg,
      max_legs_marked: 4,
    },
    boolean_modifiers_prevalence: spec.mods,
    shade_bias: shade,
    advanced_markings_bias: {
      bloody_shoulder_probability_multiplier: 1.0,
      snowflake_probability_multiplier: hasLP ? 1.0 : 0.0,
      frost_probability_multiplier: hasLP ? 1.0 : 0.0,
    },
    rating_profiles: {
      conformation: {
        head: { mean: spec.conf.head, std_dev: 7 },
        neck: { mean: spec.conf.neck, std_dev: 7 },
        shoulders: { mean: spec.conf.shoulders, std_dev: 7 },
        back: { mean: spec.conf.back, std_dev: 7 },
        hindquarters: { mean: spec.conf.hindquarters, std_dev: 6 },
        legs: { mean: spec.conf.legs, std_dev: 7 },
        hooves: { mean: spec.conf.hooves, std_dev: 6 },
      },
      gaits: {
        walk: { mean: spec.gaits.w, std_dev: 7 },
        trot: { mean: spec.gaits.t, std_dev: 8 },
        canter: { mean: spec.gaits.c, std_dev: 8 },
        gallop: { mean: spec.gaits.g, std_dev: 8 },
        gaiting: gaited ? { mean: spec.gaits.gaiting, std_dev: 8 } : null,
      },
      is_gaited_breed: gaited,
      ...(gaited ? { gaited_gait_registry: spec.gaits.reg } : {}),
    },
    temperament_weights: spec.temp,
  };

  const sqlName = spec.n.replace(/'/g, "''");
  const body = `INSERT INTO breeds (name, default_trait, breed_genetic_profile) VALUES\n('${sqlName}', 'Trait', $json$${JSON.stringify(profile, null, 2)}$json$::JSONB)\nON CONFLICT (name) DO UPDATE SET\n  default_trait = EXCLUDED.default_trait,\n  breed_genetic_profile = EXCLUDED.breed_genetic_profile,\n  updated_at = NOW();\n`;
  writeFileSync(`${DIR}/${spec.n}.txt`, body, 'utf8');
}
console.log(`\nGenerated ${B.length} breed files. Errors: ${errors}`);
